                <p class="lead text-justify whitespace-pre-line font-light text-gray-500 md:text-lg">
Siswa Jurusan Manajemen Perkantoran dan Layanan Bisnis (MPLB) atau Otomatisasi Tatakelola Perkantoran SMKN 1 Garut berhasil meraih juara pertama Lomba Office Administrartion Competition atau OACtion tingkat nasional yang diselenggarakan 23 sampai 24 Sepetember 2022 di Universitas Pendidikan Indonesia (UPI) Bandung.
Kegitan tersebut diselenggarakan oleh Asosiasi Sarjana dan Praktisi Administrasi Perkantoran (ASPAPI) Wilayah Provinsi Jawa Barat yang berkolaborasi dengan Program Studi Pendidikan Manajemen Perkantoran Fakultas Pendidikan Ekonomi dan Bisnis (FPEB) UPI serta Himpunan Mahasiswa Pendidikan Manajemen Perkantoran (HIMAPENA).
Semula lomba ini bernama Lomba Kompetensi Administrasi Perkantoran (LKAP) yang telah berjalan 3 kali hingga tahun 2019. Kini, OACtion telah disepakati ASPAPI sebagai salah satu agenda tahunan untuk diselenggarakan secara nasional, baik tingkat SMK maupun perguruan tinggi.
Jenis Lomba OACtion tahun 2022 ini terdiri dari 8 mata lomba, yaitu, Office Document Automation, Infographic Design, Typing Speed, Archive Filling, Digital Office Presentation, Data Processing, Office Administration Contest dan Public Speaking.
Lomba OACtion ini dilaksanakan 2 tahap di tingkat Provinsi dan Nasional, secara online tanggal 10 dan 11September 2022 dan tahap 2 dilaksanakan secara offline tanggal 23 dan 24 September2022 di UPI Bandung.
Pada tahap pertama dari 481 peserta yang mengikuti, siswa SMKN 1 Garut maju ke babak selanjutnya di tingkat nasional adalah Anggun Nur Rahmah mendapatkan juara 1 pada mata lomba Typing Speed, Bunga Anzeli juara 1 lomba Office Document Automation, Hansen Gracia Howardy juara 1 lomba Data Processing, Najmi Pauziah Gunawan juara 1 lomba Infografic Design.
Sedangkan pada tahap kedua yang dilaksanakan secara online diikuti oleh 47 peserta yang sebelumnya telah eliminasi dari tahap pertama yakni Siti Sarah Amelia Kelas XI MPL 2 meraih Juara 1 pada mata lomba Office Automation Contest dan Anggun Nurahmah Kelas XII MPL 2 juara harapan 1 pada mata lomba Typing Speed Tingkat Nasional yang diselenggarakan Jumat, 23 September 2022.
Mira Milansari, S.Pd., selaku Ketua Kompetensi Keahlian MPLB mengatakan bangga dengan raihan prestasi oleh siswa-siswi.
Pencapaian prestasi itu, kata dia, merupakan suatu proses yang tidak instan, tapi perlu latihan yang terus menerus.
“Alhamdulillah sangat bangga atas pencapaian prestasi ananda-ananda MPLB, ini membuktikan bahwa hasil tidak dapat membohongi proses, harapan kami ke depannya, semoga terus semangat buat belajar, belajar lagi dan terus belajar, mudah-mudahan prestasi ini dapat dipertahankan dan terus ditingkatkan lagi, karena setiap orang bisa menjadi juara,” tuturnya.
Pembimbing lainnya dari Jurusan MPLB Mira Milansari, S.Pd., Rini Anggraeni, S.Pd., Nia Marsiah, S.Pd., Sinta Safitri Purwadewi, S.Pd., dan Fitri Laelasari, S.Pd., mengaku bangga dan bersyukur serta mengapresiasi kerja keras siswa-siswi didikannya hingga meraih prestasi yang membanggakan.
Salah satu pembimbing Rini Anggraeni, S.Pd., merasa bersyukur atas apa yang sudah diraih oleh siswa-siswi bimbingannya dan berharap tahun depan bisa mendapatkan juara nasional lagi.
“Sangat bersyukur karena meskipun kita baru pertama mengikuti perlombaan ajang nasional ini, allhamdulillah bisa mendapatkan beberapa juara di tingkat nasional, mudah-mudahan kita juara umum di kompetisi lagi, tidak hanya di mata lomba Typing Speed dan Office Automation Contest saja, tapi di mata lomba lainnya di tingkat nasional dan semoga memotivasi adik-adik tingkatnya untuk meningkatkan kemampuan atau skill dari masing masing kompetensi yang dikuasai nya," katanya.***Ningrum/Broadcast                    
